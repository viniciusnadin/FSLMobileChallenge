import UIKit

extension UIColor {
        
    class func background() -> UIColor {
        return UIColor(red: 0.953, green: 0.953, blue: 0.953, alpha: 1)
    }
    
    class func lightGray() -> UIColor {
        return UIColor(red: 0.149, green: 0.196, blue: 0.22, alpha: 1)
    }
    
    class func green() -> UIColor {
        return UIColor(red: 0.094, green: 0.8, blue: 0.334, alpha: 1)
    }
    
    class func red() -> UIColor {
        return UIColor(red: 0.922, green: 0.341, blue: 0.341, alpha: 1)
    }
    
}
